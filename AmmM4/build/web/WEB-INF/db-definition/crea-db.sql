CREATE TABLE cliente (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(63),
    apellido VARCHAR(63),
    username VARCHAR(63),
    password VARCHAR(63),
    saldo REAL
);

CREATE TABLE vendedores (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(63),
    apellido VARCHAR(63),
    username VARCHAR(63),
    password VARCHAR(63),
    saldo REAL,
    feedback INTEGER
);

CREATE TABLE producto (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(50),
    descripcion VARCHAR (4095),
    foto VARCHAR(255),
    disponibilidad INTEGER,
    precio REAL,
    id_vendedores INTEGER,
    FOREIGN KEY (id_vendedores) REFERENCES vendedores(id)
);